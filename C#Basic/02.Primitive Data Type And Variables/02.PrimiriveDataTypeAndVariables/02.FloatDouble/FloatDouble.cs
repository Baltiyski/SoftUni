using System;

class FloatDouble
{
    static void Main()
    {
        float a = 12.345f;
        float b = 3456.091f;

        double c = 34.567839023;
        double d = 8923.1234857;

        Console.WriteLine("Floating numbers: \n{0} \n{1}",a,b);
        Console.WriteLine(); // for a black line betwen them
        Console.WriteLine("Double numbers: \n{0} \n{1}",c,d);
    }
}
